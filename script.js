function getAllColorsAndCodes(html) {
    const colorChipRegex = /<div class="color-chip" id="([^"]+)">[\s\S]*?value="([^"]+)"/g;
    let match;
    const colorsAndCodes = {};

    // Iterate over all matches
    while ((match = colorChipRegex.exec(html)) !== null) {
        const colorCode = match[1];
        const color = match[2];
        colorsAndCodes[colorCode] = color;
    }

    return colorsAndCodes;
}

